// authMiddleware fetches the user from the database and performs authorization using JWT session cookie.
func (a *API) authMiddleware(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		var tokenStr string

		// 1. Read from cookie
		cookie, err := r.Cookie("token")
		if err == nil {
			tokenStr = cookie.Value
		}

		// 2. Fallback to Authorization header
		if tokenStr == "" {
			authHeader := r.Header.Get("Authorization")
			if strings.HasPrefix(authHeader, "Bearer ") {
				tokenStr = strings.TrimPrefix(authHeader, "Bearer ")
			}
		}

		// 3. Fallback to query parameter (specifically for EventSource)
		if tokenStr == "" {
			tokenStr = r.URL.Query().Get("token")
		}

		if tokenStr == "" {
			http.Error(w, "Unauthorized: Missing session token", http.StatusUnauthorized)
			return
		}

		claims, err := security.VerifyJWT(tokenStr, a.jwtSecret)
		if err != nil {
			http.Error(w, "Unauthorized: Invalid or expired session token", http.StatusUnauthorized)
			return
		}

		user, err := a.service.GetUserByID(r.Context(), claims.UserID)
		if err != nil {
			http.Error(w, "Unauthorized: User not found", http.StatusUnauthorized)
			return
		}

		// Strictly enforce household isolation
		if choregroupIDStr := chi.URLParam(r, "choregroupID"); choregroupIDStr != "" {
			choregroupID, _ := uuid.Parse(choregroupIDStr)
			if claims.ChoreGroupID != choregroupID || user.ChoreGroupID != choregroupID {
				http.Error(w, "Forbidden: You do not have access to this household", http.StatusForbidden)
				return
			}
		}

		ctx := context.WithValue(r.Context(), userKey, user)
		next.ServeHTTP(w, r.WithContext(ctx))
	})
}

