func corsMiddleware(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		origin := r.Header.Get("Origin")
		if origin != "" {
			w.Header().Set("Access-Control-Allow-Origin", origin)
			w.Header().Set("Access-Control-Allow-Credentials", "true")
		} else {
			w.Header().Set("Access-Control-Allow-Origin", "*")
		}
		w.Header().Set("Access-Control-Allow-Methods", "GET, POST, PUT, DELETE, OPTIONS")
		w.Header().Set("Access-Control-Allow-Headers", "Content-Type, Authorization, X-User-ID, Cookie")

		if r.Method == "OPTIONS" {
			w.WriteHeader(http.StatusOK)
			return
		}

		next.ServeHTTP(w, r)
	})
}

// secureHeadersMiddleware injects security-related HTTP response headers.
func secureHeadersMiddleware(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Strict-Transport-Security", "max-age=63072000; includeSubDomains; preload")
		w.Header().Set("Content-Security-Policy", "default-src 'self'; style-src 'self' 'unsafe-inline' https://fonts.googleapis.com; font-src 'self' https://fonts.gstatic.com; script-src 'self' 'unsafe-inline' 'unsafe-eval'; connect-src 'self' http://localhost:8080 ws://localhost:8080 http://localhost:5173 ws://localhost:5173 http://127.0.0.1:8080 ws://127.0.0.1:8080; img-src 'self' data:;")
		w.Header().Set("X-Frame-Options", "DENY")
		w.Header().Set("X-Content-Type-Options", "nosniff")
		w.Header().Set("Referrer-Policy", "strict-origin-when-cross-origin")

		next.ServeHTTP(w, r)
	})
}



// loginDelegated logs in a kid automatically using a short-lived token.
func (a *API) loginDelegated(w http.ResponseWriter, r *http.Request) {
	var req model.DelegatedLoginRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		http.Error(w, err.Error(), http.StatusBadRequest)
		return
	}

	claims, err := security.VerifyJWT(req.Token, a.jwtSecret)
	if err != nil {
		http.Error(w, "Unauthorized: Invalid or expired login token", http.StatusUnauthorized)
		return
	}

	tokenStr, err := security.GenerateJWT(claims.UserID, claims.ChoreGroupID, claims.Role, 24*time.Hour, a.jwtSecret)
	if err != nil {
		http.Error(w, "Internal server error generating session", http.StatusInternalServerError)
		return
	}

	secure := r.TLS != nil || r.Header.Get("X-Forwarded-Proto") == "https"
	http.SetCookie(w, &http.Cookie{
		Name:     "token",
		Value:    tokenStr,
		Path:     "/",
		HttpOnly: true,
		Secure:   secure,
		SameSite: http.SameSiteStrictMode,
		MaxAge:   86400, // 24 hours
	})

	user, err := a.service.GetUserByID(r.Context(), claims.UserID)
	if err != nil {
		http.Error(w, "Unauthorized: User not found", http.StatusUnauthorized)
		return
	}

	res := model.LoginResponse{
		UserID:       claims.UserID,
		Username:     user.Username,
		ChoreGroupID: claims.ChoreGroupID,
		Role:         claims.Role,
	}
	json.NewEncoder(w).Encode(res)
}



// generateLoginLink generates a short-lived token for delegated kid login.
func (a *API) generateLoginLink(w http.ResponseWriter, r *http.Request) {
	adminUser := r.Context().Value(userKey).(model.User)
	if adminUser.Role != "admin" {
		http.Error(w, "Forbidden", http.StatusForbidden)
		return
	}

	choregroupID, _ := uuid.Parse(chi.URLParam(r, "choregroupID"))
	userID, err := uuid.Parse(chi.URLParam(r, "userID"))
	if err != nil {
		http.Error(w, "Invalid User ID", http.StatusBadRequest)
		return
	}

	targetUser, err := a.service.GetUserByID(r.Context(), userID)
	if err != nil || targetUser.ChoreGroupID != choregroupID {
		http.Error(w, "User not found in this household", http.StatusNotFound)
		return
	}

	tokenStr, err := security.GenerateJWT(targetUser.ID, targetUser.ChoreGroupID, targetUser.Role, 5*time.Minute, a.jwtSecret)
	if err != nil {
		http.Error(w, "Internal server error generating delegated link", http.StatusInternalServerError)
		return
	}

	resp := map[string]string{
		"token": tokenStr,
	}
	json.NewEncoder(w).Encode(resp)
}
